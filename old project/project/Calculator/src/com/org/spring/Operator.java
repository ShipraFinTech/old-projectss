package com.org.spring;


public class Operator {

    public int add(int a, int b) {
        return a + b;
    }

    public int sub(int a, int b) {
        return a - b;
    }

    public int multi(int a, int b) {
        return a * b;
    }

    public int division(int a, int b) {
        return a / b;
    }
}
